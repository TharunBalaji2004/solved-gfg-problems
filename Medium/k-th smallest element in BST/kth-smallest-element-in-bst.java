class Solution {

    public void inorder(Node root, int[] K, int[] ans) {
        if (root == null) return;

        inorder(root.left, K, ans);
        K[0]--;
        if (K[0] == 0) {
            ans[0] = root.data;
            return;
        }

        inorder(root.right,K, ans);
        
        
    }
    
    public int KthSmallestElement(Node root, int k) {
        int[] ans = new int[1];
        int[] K = new int[1];
        ans[0] = -1;
        K[0] = k;
        inorder(root, K, ans);
        return ans[0];
    }
}
